    <h1><i></i> О застройщике <i></i></h1>
    <div class="about-logo"></div>
    <p>Строительная  компания АО «ТУС», основанная в 1992 году на протяжении многих лет уверенно сохраняет позицию лидера на рынке недвижимости Республики. Приоритетное направление компании – строительство и реализация качественного жилья по доступным ценам. В настоящее время компанией активно ведется строительство Микрорайона «Университетский -2». Его площадь – более 375 000 кв.м. Население – 13 000 человек.</p>
    <p>В  2013 компания успешно реализовала инвестиционный проект Завод строительной керамики «КЕТРА». Основной продукцией завода является облицовочный кирпич и крупноформатные блоки. Производственная мощность завода продолжает расти и набирать обороты. Сегодня на заводе выпускается более 20 видов продукции. Планируется запуск второй линии производства, расширение ассортимента предлагаемой продукции.</p>
    <p>Строительная  компания АО «ТУС» имеет в своих активах собственное производство, что позволяет контролировать все этапы от производства стройматериалов, разработки проекта и строительства непосредственно самих объектов. АО «ТУС» является победителем многих наград и премий, но лучшая награда для нас – это счастливые жители.</p>
